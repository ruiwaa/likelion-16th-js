// - 응답(Response)은 상태 코드(Status), 헤더, 바디(Payload)를 돌려줍니다.
// - 2xx(성공), 4xx(클라이언트 잘못), 5xx(서버 잘못) 등 상태 코드로 결과를 파악합니다.
// - CRUD(생성, 읽기, 수정, 삭제) 작업은 각각 POST, GET, PUT/PATCH, DELETE 메서드와 대응합니다.
// - fetch() 사용 시 GET 외의 메서드는 반드시 옵션 객체에 method를 명시해야 합니다.
