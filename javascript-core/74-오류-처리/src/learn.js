// --------------------------------------------------------------------------
// - fetch()는 네트워크 장애가 아니면 404, 500 에러도 resolve로 간주합니다.
// - response.ok(200-299) 속성을 통해 성공 여부를 수동으로 확인해야 합니다.
// - 에러 시에도 서버가 보내는 body 데이터가 중요하므로 response.json()을 먼저 처리합니다.
// - 명확하고 친절한 오류 메시지(Case별 처리)는 사용자 경험(UX)을 크게 향상시킵니다.
// - 에러 객체를 reject할 때 충분한 정보(status, body 등)를 담아 전달하는 것이 좋습니다.
