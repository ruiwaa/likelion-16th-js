// - Axios는 HTTP 상태 코드가 오류 범위(4xx, 5xx)일 경우 자동으로 .catch() 블록을 실행하여 오류 처리가 간편합니다.
// - 프로젝트 규모가 크고 코드 가독성이 중요하다면 Axios를, 외부 의존성을 줄이고 싶다면 Fetch API 사용을 권장합니다.
