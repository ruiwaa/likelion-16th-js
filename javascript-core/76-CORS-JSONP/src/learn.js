// --------------------------------------------------------------------------
// 핵심 요약
// --------------------------------------------------------------------------
// - CORS(교차 출처 리소스 공유)는 브라우저가 다른 출처의 리소스 접근을 제한하는 보안 정책입니다.
// - 출처(Origin)는 프로토콜, 호스트, 포트 세 가지가 모두 일치해야 '동일 출처'로 인정됩니다.
// - 서버는 Access-Control-Allow-Origin 헤더를 통해 특정 출처의 접근 허용 여부를 결정합니다.
// - JSONP는 <script> 태그의 src 속성이 출처 제약을 받지 않는 점을 이용한 우회 방법입니다 (GET만 가능).
// - 프록시(Proxy) 서버는 클라이언트 대신 외부 서버에 요청을 보내 데이터를 받아옴으로써 CORS 문제를 해결합니다.
