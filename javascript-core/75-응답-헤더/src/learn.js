// - 요청 헤더(Request)는 클라이언트가 서버에 정보를 제공할 때 사용합니다.
// - 응답 헤더(Response)는 서버가 데이터의 타입, 보안 정책, 캐싱 설정 등을 알려줄 때 사용합니다.
// - fetch API에서는 response.headers 객체를 통해 이 정보에 접근할 수 있습니다.
// - headers.get('content-type') 처럼 특정 헤더만 개별적으로 가져올 수도 있습니다.
// - 보안상 클라이언트에서 접근할 수 있는 응답 헤더는 서버의 CORS 설정에 따라 제한될 수 있습니다.
